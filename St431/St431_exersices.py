def simple_compression_alt():
    s = input()
    result = ''
    count = 1

    if len(s) == 1:
        result = s

    for i in range(1, len(s)):
        if s[i] == s[i - 1] and i == len(s) - 1:
            result += str(count + 1) + s[i - 1]
        if s[i] == s[i - 1]:
            count += 1
        elif s[i] != s[i - 1] and i == len(s) - 1:
            if count == 1:
                result += s[i - 1] + s[i]
            else:
                result += str(count) + s[i - 1] + s[i]
        else:
            if count == 1:
                result += s[i - 1]
            else:
                result += str(count) + s[i - 1]
                count = 1

    print(result)

simple_compression_alt()

