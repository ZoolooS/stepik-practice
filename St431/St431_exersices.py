def length_converter():
    s = input().split()
    l_units = {'mm': 0.001, 'cm': 0.01, 'm': 1, 'km': 1000,
               'inch': 0.0254, 'foot': 0.3048, 'yard': 0.9144, 'mile': 1609}

    print(f'{float(s[0]) * l_units[s[1]] / l_units[s[3]]:.2e}')

