def s_c_cutter(s):
    char = s[0]
#    if len(s) == 1:
#        return char, []
    for i in range(len(s)):
        if i == len(s) - 1:
            return char, []
        elif s[i] != char:
            if i == 1:
#                print(f'{char} ==> {s[i:]}')
                return char, s[i:]
            else:
#                print(f'{str(i) + char} ==> {s[i:]}')
                return str(i) + char, s[i:]


def simple_compression():
    s = input()
    s_cut = s[:]
    result = ''

    while s_cut:
        part, s_cut = s_c_cutter(s_cut)
        result += part

    print(result)

